create table illustratie
(
    id   bigint not null primary key,
    href text,
    type text,
    naam text,
    legendanaam text,
    structuurvisiegebied_id bigint
);

create index illustratie_type_idx on illustratie (type);
create index illustratie_naam_idx on illustratie (naam);
create index illustratie_structuurvisiegebied_idx on illustratie (structuurvisiegebied_id);

