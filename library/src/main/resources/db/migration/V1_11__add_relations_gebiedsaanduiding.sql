create index gebiedsaanwijzing_artikel_artikel_idx on gebiedsaanwijzing_artikel (artikel_id);

ALTER TABLE public.gebiedsaanwijzing_artikel
    OWNER TO testuser;

ALTER TABLE public.gebiedsaanwijzing_artikel
    ALTER
        COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY (
        SEQUENCE NAME public.gebiedsaanwijzing_artikel_id_seq
        START WITH 1
        INCREMENT BY 1
        NO MINVALUE
        NO MAXVALUE
        CACHE 1
        );

