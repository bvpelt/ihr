-- table maatvoering
create table maatvoering
(
    id                  bigint not null primary key,
    planidentificatie   text   not null,
    identificatie       text   not null,
    naam                text,
    verwijzingnaartekst text,
    styleid             text,
    md5hash             text
);

create index maatvoering_planidentificatie_idx on maatvoering (planidentificatie);

create index maatvoering_identificatie_idx on maatvoering (identificatie);

create unique index maatvoering_planidentificatie_identificatie_idx on maatvoering (planidentificatie, identificatie);

ALTER TABLE maatvoering
    OWNER TO testuser;

ALTER TABLE maatvoering
    ALTER
        COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY (
        SEQUENCE NAME maatvoering_id_seq
        START WITH 1
        INCREMENT BY 1
        NO MINVALUE
        NO MAXVALUE
        CACHE 1
        );


